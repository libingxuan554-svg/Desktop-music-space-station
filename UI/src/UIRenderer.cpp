void UIRenderer::renderMultiBarEQ(FramebufferUI* ui, float intensity, int cx, int cy, int maxH) {
    const int numBars = 21;    
    const int barW = 16;       
    const int spacing = 10;     
    const int totalW = numBars * barW + (numBars - 1) * spacing;
    const int startX = cx - totalW / 2;
    static float barHeights[numBars] = {0};
    static float personality[numBars] = {0.7f, 1.4f, 0.6f, 1.1f, 1.8f, 0.9f, 1.3f, 0.5f, 1.6f, 1.0f, 1.9f, 0.8f, 1.4f, 0.7f, 1.2f, 0.6f, 1.7f, 0.9f, 1.5f, 0.5f, 1.1f};
    static float noiseFrame = 0.0f;
    noiseFrame += 0.15f;
    if (intensity < 0.02f) {
        for (int i = 0; i < numBars; ++i) {
            barHeights[i] *= 0.82f;
            int h = (int)barHeights[i] + 5;
            ui->drawRect(startX + i * (barW + spacing), cy - h, barW, h, {0, 60, 80});
        }
        return;
    }
    for (int i = 0; i < numBars; ++i) {
        float noise = std::abs(std::sin(noiseFrame + i)) * 0.05f;
        float targetH = (intensity + noise) * maxH * personality[i];
        if (targetH > barHeights[i]) barHeights[i] = targetH;
        else barHeights[i] -= (barHeights[i] - targetH) * 0.25f;
        int h = static_cast<int>(barHeights[i]);
        h = std::clamp(h, 5, maxH);
        ui->drawRect(startX + i * (barW + spacing), cy - h, barW, h, {0, 255, 220});
        ui->drawRect(startX + i * (barW + spacing), cy - h - 5, barW, 2, {200, 255, 255});
    }
}

// ============== PLAYER 原版画字魔法 (冻结保护) ==============
void UIRenderer::drawText(FramebufferUI* ui, const std::string& text, int x, int y, Color color, int scale, int clipX1, int clipX2) {
    for (size_t i = 0; i < text.length(); ++i) {
        unsigned char c = text[i];
        if (c < 32 || c > 127) c = 63; 
        const unsigned char* glyph = font8x8[c - 32];
        for (int col = 0; col < 8; ++col) {
            int pixelX = x + (i * 8 + col) * scale;
            if (pixelX < clipX1 || pixelX + scale > clipX2) continue; 
            for (int row = 0; row < 8; ++row) {
                if (glyph[row] & (1 << (7 - col))) {
                    ui->drawRect(pixelX, y + row * scale, scale, scale, color);
                }
            }
        }
    }
}

// ============== MUSIC_LIST 专属带上下遮挡的画字黑科技（g冻结保护） ==============
void UIRenderer::drawListText(FramebufferUI* ui, const std::string& text, int x, int y, Color color, int scale, int clipX1, int clipX2, int clipY1, int clipY2) {
    for (size_t i = 0; i < text.length(); ++i) {
        unsigned char c = text[i];
        if (c < 32 || c > 127) c = 63; 
        const unsigned char* glyph = font8x8[c - 32];
        for (int col = 0; col < 8; ++col) {
            int pixelX = x + (i * 8 + col) * scale;
            if (pixelX < clipX1 || pixelX + scale > clipX2) continue; 
            for (int row = 0; row < 8; ++row) {
                if (glyph[row] & (1 << (7 - col))) {
                    // 使用底层的裁剪绘制，碰到边界像刀切一样整齐，且完全不消耗多余CPU
                    drawClippedRect(ui, pixelX, y + row * scale, scale, scale, color, clipY1, clipY2);
                }
            }
        }
    }
}


