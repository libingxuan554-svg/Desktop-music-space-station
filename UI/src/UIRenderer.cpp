// 🌟 STANDBY 专属：渐变分段内存条
void UIRenderer::renderStandbyMemoryBar(FramebufferUI* ui, int x, int y, int w, int h, int percent) {
    int segments = 10; // 分成10个数据块
    int gap = 4;
    int segW = (w - (segments - 1) * gap) / segments;

    int activeSegs = static_cast<int>(std::ceil((percent / 100.0f) * segments));

    for (int i = 0; i < segments; ++i) {
        Color col = {0, 80, 100}; // 默认未激活的暗色槽
        if (i < activeSegs) {
            if (i < 4) col = {0, 255, 255};           // 前40%：赛博青色
            else if (i < 7) col = {255, 240, 150};    // 40%-70%：警戒米黄色
            else col = {255, 100, 100};               // 70%以上：危险红色
        }
        ui->drawRect(x + i * (segW + gap), y, segW, h, col);
    }
}

// 🌟 STANDBY 专属：纯代码绘制像素风天气图标
void UIRenderer::renderWeatherIcon(FramebufferUI* ui, int x, int y, int weatherCode) {
    Color cyan = {0, 255, 255};
    Color yellow = {255, 240, 150};
    Color white = {255, 255, 255};
    
    // 0: 晴天 (SUNNY) - 不画云，只画一个大太阳
    if (weatherCode == 0) {
        ui->drawRect(x + 25, y + 5, 20, 20, yellow);
        ui->drawRect(x + 33, y - 5, 4, 6, yellow);  // 上光芒
        ui->drawRect(x + 33, y + 29, 4, 6, yellow); // 下光芒
        ui->drawRect(x + 13, y + 13, 6, 4, yellow); // 左光芒
        ui->drawRect(x + 51, y + 13, 6, 4, yellow); // 右光芒
        return; // 晴天画完直接返回
    }

    // 其他天气：都画一个基础的云朵外形作为底座
    ui->drawRect(x + 15, y, 30, 8, cyan);
    ui->drawRect(x + 5, y + 8, 50, 16, cyan);
    ui->drawRect(x, y + 24, 60, 8, cyan);

    // 根据天气代码增加特效
    if (weatherCode == 1) { 
        // 1: 多云 (CLOUDY) - 画一个小太阳在云朵右上角半遮挡
        ui->drawRect(x + 50, y - 5, 12, 12, yellow);
    } 
    else if (weatherCode == 2) { 
        // 2: 下雨 (RAINY) - 倾斜的雨滴
        ui->drawRect(x + 10, y + 38, 4, 10, cyan);
        ui->drawRect(x + 30, y + 38, 4, 10, cyan);
        ui->drawRect(x + 50, y + 38, 4, 10, cyan);
    }
    else if (weatherCode == 3) { 
        // 3: 下雪 (SNOWY) - 错落的白色雪花点
        ui->drawRect(x + 15, y + 40, 6, 6, white);
        ui->drawRect(x + 38, y + 36, 6, 6, white);
        ui->drawRect(x + 25, y + 50, 6, 6, white);
        ui->drawRect(x + 48, y + 48, 6, 6, white);
    }
    else if (weatherCode == 4) { 
        // 4: 雷暴 (STORM) - 赛博黄色的闪电
        ui->drawRect(x + 28, y + 35, 8, 10, yellow);
        ui->drawRect(x + 22, y + 45, 10, 4, yellow);
        ui->drawRect(x + 24, y + 49, 6, 12, yellow);
    }
    else if (weatherCode == 5) { 
        // 5: 雾霾 (FOGGY) - 云朵下方的平行雾气线条
        ui->drawRect(x + 5, y + 40, 50, 4, white);
        ui->drawRect(x + 15, y + 48, 40, 4, white);
        ui->drawRect(x + 10, y + 56, 30, 4, white);
    }
}

// 🌟 STANDBY 专属：拓扑图呼吸节点灯 (已修改为精确匹配底图的 8 个节点)
void UIRenderer::renderTopologyBreathingLights(FramebufferUI* ui, int cx, int cy, float phase) {
    // 利用 sin 函数计算呼吸强度 (0.0 - 1.0)
    float pulse = (std::sin(phase) + 1.0f) * 0.5f;
    int glow = static_cast<int>(pulse * 255.0f);
    Color pulseCol = {0, (uint8_t)glow, 255}; // 呼吸青色

    // 🌟 核心修改：定义 8 个节点相对于中心点 (cx, cy) 的精确坐标偏移量 [dx, dy]
    // 估算值：内圈对角线半径约 42 (dx/dy 约 30)，外圈十字半径约 78
    // 如果编译后发现没有完全盖住底图的圆圈，请稍微微调这里的数字！
    const int offsets[8][2] = {
        // 内圈的 4 个节点 (左上, 右上, 左下, 右下)
        {-30, -30}, { 30, -30},
        {-30,  30}, { 30,  30},

        // 外圈的 4 个节点 (上, 下, 左, 右)
        {  0, -78}, {  0,  78},
        {-78,   0}, { 78,   0}
    };

    // 遍历这 8 个固定的坐标点，画出呼吸灯
    for (int i = 0; i < 8; ++i) {
        int nx = cx + offsets[i][0];
        int ny = cy + offsets[i][1];
        // 画一个 6x6 的光点，使其完美盖在底图的圆圈上
        ui->drawRect(nx - 3, ny - 3, 6, 6, pulseCol);
    }
}

