// 🌟 私有内部黑科技：带 Y 轴上下限的坐标像素截断器，性能极高！
static void drawClippedRect(FramebufferUI* ui, int x, int y, int w, int h, Color color, int clipY1, int clipY2) {
    int startY = std::max(y, clipY1);
    int endY = std::min(y + h, clipY2);
    if (startY < endY && w > 0) {
        ui->drawRect(x, startY, w, endY - startY, color);
    }
}

void UIRenderer::renderTechBar(FramebufferUI* ui, float value, int x, int y, int maxW) {
    int barLen = std::min(static_cast<int>(value * (maxW / 50.0f)), maxW);
    ui->drawRect(x, y, barLen, 40, {255, 100, 0}); 
}

void UIRenderer::renderMirrorEqualizer(FramebufferUI* ui, float intensity, int cx, int cy, int maxH) {
    int h = static_cast<int>(intensity * maxH);
    ui->drawRect(cx - 60, cy - h, 40, h * 2, {0, 255, 150}); 
}

// ============== PLAYER 专属极简 1px 边框 (冻结保护) ==============
void UIRenderer::renderButton(FramebufferUI* ui, const UIButton& btn, bool isHighlighted) {
    if (btn.y + btn.h < 0 || btn.y > ui->getHeight()) return;
    Color frameColor = isHighlighted ? Color{255, 240, 150} : Color{0, 200, 255}; 
    ui->drawRect(btn.x, btn.y, btn.w, 1, frameColor);           
    ui->drawRect(btn.x, btn.y + btn.h - 1, btn.w, 1, frameColor); 
    ui->drawRect(btn.x, btn.y, 1, btn.h, frameColor);           
    ui->drawRect(btn.x + btn.w - 1, btn.y, 1, btn.h, frameColor); 
}

// ============== MUSIC_LIST 专属带上下遮罩的赛博切角框（冻结保护） ==============
void UIRenderer::renderListButton(FramebufferUI* ui, int x, int y, int w, int h, bool isSelected, int clipY1, int clipY2) {
    // 粗略过滤完全看不见的框，节省循环
    if (y + h < clipY1 || y > clipY2) return;
    Color cyan = {0, 255, 255};
    int c = 15; 

    if (isSelected) {
        for(int i=0; i<c; ++i) {
            drawClippedRect(ui, x + c - i, y + i, w - 2*c + 2*i, 1, cyan, clipY1, clipY2); 
            drawClippedRect(ui, x + c - i, y + h - 1 - i, w - 2*c + 2*i, 1, cyan, clipY1, clipY2); 
        }
        drawClippedRect(ui, x, y + c, w, h - 2*c, cyan, clipY1, clipY2); 
    } else {
        drawClippedRect(ui, x + c, y, w - 2*c, 2, cyan, clipY1, clipY2); 
        drawClippedRect(ui, x + c, y + h - 2, w - 2*c, 2, cyan, clipY1, clipY2); 
        drawClippedRect(ui, x, y + c, 2, h - 2*c, cyan, clipY1, clipY2); 
        drawClippedRect(ui, x + w - 2, y + c, 2, h - 2*c, cyan, clipY1, clipY2); 
        for(int i=0; i<c; ++i) {
            drawClippedRect(ui, x + c - i, y + i, 2, 2, cyan, clipY1, clipY2); 
            drawClippedRect(ui, x + w - c + i - 2, y + i, 2, 2, cyan, clipY1, clipY2); 
            drawClippedRect(ui, x + c - i, y + h - 1 - i, 2, 2, cyan, clipY1, clipY2); 
            drawClippedRect(ui, x + w - c + i - 2, y + h - 1 - i, 2, 2, cyan, clipY1, clipY2); 
        }
    }
}

